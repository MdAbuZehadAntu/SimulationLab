import random
import math
import matplotlib.pyplot as plt

n=[500,1000,5000]
l=float(input("Enter the value of l : "))
d=float(input("Enter the value of d : "))

for item in n:
  hits=0

  x1=[]
  y1=[]
  x2=[]
  y2=[]

  for i in range(item):
    D=random.uniform(0,d/2)
    theta=random.uniform(0,180)

    if D <= (l/2)*math.sin(theta*math.pi/180):
      hits+=1
      y1.append(D)
      x1.append(theta*math.pi/180)
    else:
      y2.append(D)
      x2.append(theta*math.pi/180)

      
   
      

    
      

  pi_res=(2*l/d)*item/hits
  plt.xlabel("value of theta")
  plt.ylabel("D")
  plt.title(f"Buffon's Needle Experiment for {item} iterations")
  plt.scatter(x1,y1,color="red")
  plt.scatter(x2,y2,color="green")
  plt.legend(["Hit Points","Miss Points"],loc="upper right")
  plt.show()

  print(f"For {item} iterations : the value of pi is {pi_res}")